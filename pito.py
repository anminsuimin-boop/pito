import streamlit as st
import random

# ページの設定
st.set_page_config(page_title="PitoBox", page_icon="🐨")

# タイトル
st.title("🐨 PitoBox 🐨")
st.write("お嬢様、今の気分を教えてください！")

# スタイル調整（スマホでボタンを見やすく）
st.markdown("""
<style>
div.stButton > button {
    width: 100%;
    margin-bottom: 10px;
    font-weight: bold;
    border-radius: 12px;
    padding: 15px;
}
</style>
""", unsafe_allow_html=True)

# -------------------------------------------------
# 1. 甘々モード（Sweet）
# -------------------------------------------------
